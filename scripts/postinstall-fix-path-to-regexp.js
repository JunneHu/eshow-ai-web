/**
 * Some transitive deps hard-reference `path-to-regexp/index.js`.
 * But path-to-regexp v6+ ships entrypoints under `dist/`.
 * If `index.js` is missing, webpack-dev-server may crash with ENOENT.
 *
 * This postinstall script creates a tiny compat file:
 *   node_modules/path-to-regexp/index.js -> require('./dist/index.js')
 *
 * Safe to run multiple times.
 */
const fs = require('fs');
const path = require('path');

function ensureCompatIndex() {
  const pkgDir = path.join(__dirname, '..', 'node_modules', 'path-to-regexp');
  const target = path.join(pkgDir, 'index.js');
  const dist = path.join(pkgDir, 'dist', 'index.js');

  if (!fs.existsSync(pkgDir)) return;
  if (fs.existsSync(target)) return;
  if (!fs.existsSync(dist)) return;

  const content = [
    "/* auto-generated by scripts/postinstall-fix-path-to-regexp.js */",
    "module.exports = require('./dist/index.js');",
    '',
  ].join('\n');

  fs.writeFileSync(target, content, 'utf8');
}

try {
  ensureCompatIndex();
} catch (e) {
  // Don't fail install for a best-effort compat shim
}


